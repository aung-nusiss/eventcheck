export function createWalkIn({ name, email, company, ticket }) {
  return {
    id: crypto.randomUUID(),
    qrCode: `QR-WALKIN-${Date.now()}`,
    name,
    email,
    company: company || '',
    ticket: ticket || 'general',
    checkedIn: true,
    checkedInAt: new Date().toISOString(),
    isWalkIn: true,
  }
}
